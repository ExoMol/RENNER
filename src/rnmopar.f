C
      INTEGER FUNCTION IFACT(K)
      INTEGER I,K,J
      I=1
      IF(K.LT.2)GOTO 2
      DO 1 J=2,K
1     I=I*J
2     IFACT=I
      RETURN
      END
C
C
      DOUBLE PRECISION FUNCTION FACLOG(K)
      DOUBLE PRECISION XDUM
      INTEGER I,K,J
      XDUM=0.0D+00
      IF(K.LT.2)GOTO 2
      DO 1 J=2,K
1     XDUM=XDUM+LOG(DFLOAT(J))
2     FACLOG=XDUM
      RETURN
      END
