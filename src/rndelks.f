      SUBROUTINE DELK0 ( EV2K   , ESTR   , VALSYM , VALASY ,
     1                   SMAT   , HMAT   , NOFSET , NDIMST ,
     1                   JP1    ,KKP1    , IRODIM , IEO    , ITAU   ,
     1                   INTSYM , INTASY , ISTOFF ,
     1                   NOBAS  , NOPERS , NZERSY , NZERAS , LREC35 )
      IMPLICIT REAL*8 (A-H,O-Z)
C
      REAL*8 M1,M2,M3,M,U1,U3,U13,V,RHO,EPS,
     1      EPSP,EPSPP,EPSPPP,CR,SR,CSE,SNE,
     2      CRE,SRE,CORO
C
      REAL*8 AMASS(3,10)
C
      REAL*8 RHOE,FA1,FA2,FA3,FA4,FA5,FA6,FA7,FA8,
     1       AA1,AA3,
     2       F1A1,F2A1,F3A1,F4A1,F1A3,F2A3,F3A3,F4A3,
     3       F11,F1A11,F2A11,F3A11,F33,F1A33,F2A33,F3A33,
     4       F13,F1A13,F2A13,F3A13,
     5       F111,F1A111,F2A111,F333,F1A333,F2A333,
     6       F113,F1A113,F2A113,F133,F1A133,F2A133,
     7       F1111,FA1111,F3333,FA3333,F1113,FA1113,
     8       F1333,FA1333,F1133,FA1133,
     8       RE12 , RE32 , RHOREF , VMIN
C
      REAL*8 ETRIAL , RHOMAX , PNM1 , HBASE , HSTEP , EGUESS ,
     1      PREC
C
      REAL*8 THRSH1 , THRSH2 , THRSH3 , THRSH4 , THRSH5 ,
     1      THRSH6 , THRSH7 , THRSH8 , THRSH9 , THRSHX ,
     2      VELLGT , PLANCK , AVOGNO , DEGRAD , RADDEG ,
     3      PI
C
      REAL*8   B11,B13,B111,B133,B113,
     2      B1111,B1333,B1113,B1133,
     3      B11111,B13333,B11113,B11333,B11133,
     4      B31,B33,B311,B333,B313,
     5      B3111,B3333,B3113,B3133,
     6      B31111,B33333,B31113,B31333,B31133
C
      REAL*8   CR1,CR3,CR11,CR33,CR13,
     2      CR111,CR333,CR113,CR133,
     3      CR1111,CR3333,CR1113,CR1333,CR1133
C
      INTEGER NFIL1 , NFIL2 , NFIL3 , NFIL4 , NFIL5 , NFIL6 ,
     5       NFIL7 , NFIL8 , NFIL9 , NFIL10 ,
     6       NFIL11 , NFIL12 , NFIL13 , NFIL14 , NFIL15 ,
     7       NFIL16 , NFIL17 , NFIL18 , NFIL19 , NFIL20 ,
     6       ITEST  , IPRINT , NSTNR , NSTNIN , IREST ,
     7       IISOT , IQUAS , ISYMS , NISOT , NQUAS ,
     8       NUMQUA , NOPTIT , NOPTIM , IOBSER , NOBSER,
     9       ISOMAX , NATTS  , V0TYPE , IVAR(128) , PARMAX ,
     1       NUMPAR , PRTINT
C
      INTEGER V1 ,V2, V3 , V2MXP1, V2P1,
     1       NSTINT , NSERIN , NSERP , NSERQ , KQUA , NTEST ,
     2       NSEPP2 , NSEQP1 , MBASIS ,
     3       MDIM , NFSYM0, NFASY0, NFSYMJ, NFASYJ,
     4       KSTYPA(2) , LSTYPA(2) , JMAX , V2MAX , JMAXP1
C
      INTEGER IQUANT(9,10)
C
      LOGICAL SYMM
C
      REAL*8 RMK(2),AAS(2)
      INTEGER NOBAS,MBASP1,LENIW
      INTEGER LENINT,IOSC1,IOSC3,I,J,IR,JL
      REAL*8 G,GC1,GC3,GC1113,GC3133,GC1133,GC3113
      REAL*8  EV2K(V2MXP1,JMAXP1),ESTR(NOBAS),
     1       SMAT(NOBAS,NOBAS,NOPERS),VALSYM(INTSYM),VALASY(INTASY),
     1       HMAT(LREC35,LREC35)
      REAL*8  CONCOR,CONFC1,CONG11,CONG33,CONG13,CORHOE
      COMMON /ISOTOP/ IQUANT,AMASS
C
      COMMON /VALUES/ RHO,EPS,EPSP,EPSPP,EPSPPP,
     1               CR,SR,CSE,SNE,CRE,SRE,CORO
C
      COMMON /MOLCUL/ RHOE,FA1,FA2,FA3,FA4,FA5,FA6,FA7,FA8,
     1               AA1,AA3,
     1               F11,F33,F13,F111,F333,F113,F133,
     1               F1111,F3333,F1113,F1333,F1133,
     2               F1,F1A1,F2A1,F3A1,F4A1,F3,F1A3,F2A3,F3A3,F4A3,
     3               F1A11,F2A11,F3A11,F1A33,F2A33,F3A33,
     4               F1A13,F2A13,F3A13,
     5               F1A111,F2A111,F1A333,F2A333,
     6               F1A113,F2A113,F1A133,F2A133,
     7               FA1111,FA3333,FA1113,
     8               FA1333,FA1133, R12RF1, R32RF1, R12RF2, R32RF2,
     8               RE12 , RE32 , M1 , M2 , M3 , M ,
     9               U1 , U3 , U13 , V ,
     1               SYMM
      COMMON /RENTEL/G1,G3,G11,G33,G13,G111,G333,G113,G133,
     1               G1111,G3333,G1113,G1333,G1133,
     2               GA1,GA2,GA3,GA4,GA5,GA6,GA7,GA8,
     2               G1A1,G2A1,G3A1,G4A1,G1A3,G2A3,G3A3,G4A3,
     3               G1A11,G2A11,G3A11,G1A33,G2A33,G3A33,
     4               G1A13,G2A13,G3A13,
     5               G1A111,G2A111,G1A333,G2A333,
     6               G1A113,G2A113,G1A133,G2A133,
     7               GA1111,GA3333,GA1113,
     8               GA1333,GA1133,
     1               HA1,HA2,HA3,HA4,HA5,HA6,HA7,HA8,
     2               H1A1,H2A1,H3A1,H4A1,H1A3,H2A3,H3A3,H4A3,
     3               H1A11,H2A11,H3A11,H1A33,H2A33,H3A33,
     4               H1A13,H2A13,H3A13,
     5               H1A111,H2A111,H1A333,H2A333,
     6               H1A113,H2A113,H1A133,H2A133,
     7               HA1111,HA3333,HA1113,
     8               HA1333,HA1133
C
      COMMON /INTEG/  ETRIAL , RHOMAX , PNM1 , HBASE , HSTEP , EGUESS ,
     1               RHOREF , VMINS1 , VMINS2 , ZTRIAL(2) , V0TYPE ,
     1               NSTINT , NSERIN , NSERP , NSERQ , KQUA , NTEST ,
     2               NSEPP2 , NSEQP1 , KSTYPA , LSTYPA
C
      COMMON /DIMEN/ NSURF,  MBASIS ,  V2MAX  , V2MXP1 ,
     1               JMAX   , JMAXP1 , MDIM   , NFSYM0 , NFASY0 ,
     2               NFSYMJ , NFASYJ , NFINTA
C
      COMMON /LSFIT/  PARMAX , NUMPAR , ISOMAX , IVAR
C
      include 'rensys.h'
C
      COMMON/BCOEFF/
     1      B11,B13,B111,B133,B113,
     2      B1111,B1333,B1113,B1133,
     3      B11111,B13333,B11113,B11333,B11133,
     4      B31,B33,B311,B333,B313,
     5      B3111,B3333,B3113,B3133,
     6      B31111,B33333,B31113,B31333,B31133
C
      COMMON/CRCOEF/
     1      CR1,CR3,CR11,CR33,CR13,
     2      CR111,CR333,CR113,CR133,
     3      CR1111,CR3333,CR1113,CR1333,CR1133
C
C
      COMMON/MORSE/RMK,AAS
C
      CONCOR=PLANCK*AVOGNO*1.0E+16/(1.6E+01*PI*PI*VELLGT)
      CONRR=2.0D+00*CONCOR
      CONXX=CONCOR
      CONZZ=2.0D+00*CONXX
      CONFC1=2.0D+00*CONCOR
      CONG13=-CONFC1/M2
      J=JP1-1
      CORHOE=COS(RHOE)
      KNDSYM=0
      IF (.NOT. SYMM) KNDASY=0
C
      XJJP1=DFLOAT(J*JP1)
      IF (MOD(J+ITAU,2) .NE. 0) XJJP1=-XJJP1
C
      DO 940 KP1=1,JP1
C
C
      XJK=DFLOAT(J*JP1-(KP1-1)*(KP1-1))
C
C
      DO 920 INDEX1=1,V2MXP1
      DO 900 INDEX2=1,V2MXP1
C
      KNDSYM=KNDSYM+1
      IOFSY=(KNDSYM-1)*NZERSY
      IF (.NOT. SYMM) THEN
           KNDASY=KNDASY+1
           IOFAS=(KNDASY-1)*NZERAS
      ENDIF
      IF (MOD(KP1,2) .EQ. IEO) GOTO 900
      IF (KP1.NE.KKP1) GOTO 900
      IF (MOD(J+ITAU,2) .NE. 0  .AND. KP1 .EQ. 1)  GOTO 900
C
C
      KNDEX=0
C
      IF (MOD(KP1,2) .NE. 0) THEN
            INDK=(KP1-1)/2
            IF (MOD(J+ITAU,2) .NE. 0) INDK=INDK-1
      ELSE
            INDK=(KP1-2)/2
      ENDIF
      IROWOF=NOFSET+(INDEX1-1)*NDIMST
      ICOLOF=NOFSET+(INDEX2-1)*NDIMST
C
C  0. ZERO ORDER ENERGIES ADDED IN DIAGONAL ELEMENTS
C
      IF (INDEX1 .EQ. INDEX2) THEN
      DO  50 IR=1,NDIMST
50    HMAT(IROWOF+IR,IROWOF+IR)=HMAT(IROWOF+IR,IROWOF+IR)
     1       + EV2K(INDEX1,KP1)+ESTR(ISTOFF+IR)
      ENDIF
C
C  1. LEADING TERM (NOT DEPENDING ON STRETCHING COORDINATES)
C     CONTRIBUTIONS FROM:  A0. MURR0
C                           A. MUXX0+MUYY0
C                          A2. MUZZ0
C                           B. CORIOLIS COMMUTATOR TERMS
C                           C. G MATRIX COMMUTATOR TERMS
C                           D. PSEUDOPOTENTIAL TERM
C
      G = CONRR*VALSYM(IOFSY+1)+VALSYM(IOFSY+10)
     1 + CONXX*(VALSYM(IOFSY+32)+VALSYM(IOFSY+41))*XJK
     2 + CONZZ*VALSYM(IOFSY+50)
      GCOR1= CONCOR*AA1*VALSYM(IOFSY+23)
      IF (SYMM) THEN
            GCOR3=GCOR1
      ELSE
            GCOR3= CONCOR*AA3*VALASY(IOFAS+16)
      ENDIF
      GGMAT=-CONG13*AA1*AA3*VALSYM(IOFSY+22)/2.0E+00
      IF (KP1 .EQ. 2)
     1      G = G + 0.5E+00*CONXX*VALSYM(IOFSY+59)*XJJP1
      DO 100 IR=1,NDIMST
      HMAT(ICOLOF+IR,IROWOF+IR)=HMAT(ICOLOF+IR,IROWOF+IR)
     1                       +GCOR1+GCOR3
100   HMAT(IROWOF+IR,ICOLOF+IR)=HMAT(IROWOF+IR,ICOLOF+IR)
     1                       +G +GCOR1+GCOR3+GGMAT
C
C  2. TERMS CONTAINING Y1
C     CONTRIBUTIONS FROM:   A. MU(RHO,RHO)0
C                           B. THE POTENTIAL ENERGY FUNCTION
C                           C. MUXX0+MUYY0
C                           D. MUZZ0
C                           E. CORIOLIS COMMUTATOR TERMS
C                           F. G MATRIX COMMUTATOR TERMS
C
      KNDEX=KNDEX+1
      G = CONRR*VALSYM(IOFSY+2)
     1  + VALSYM(IOFSY+11)
     1  + CONXX*(VALSYM(IOFSY+33)+VALSYM(IOFSY+42))*XJK
     2  + CONZZ*VALSYM(IOFSY+51)
      GCOR1=-CONCOR*AA1*VALSYM(IOFSY+23)
     1     + 2.0E+00*CONCOR*AA1*VALSYM(IOFSY+25)
      IF (SYMM) THEN
            GCOR3= CONCOR*AA1*VALSYM(IOFSY+27)
      ELSE
            GCOR3= CONCOR*AA3*VALASY(IOFAS+19)
      ENDIF
      GGMAT=CONG13*AA1*AA3*VALSYM(IOFSY+22)/2.0E+00
      IF (KP1 .EQ. 2)
     1      G = G + 0.5E+00*CONXX*VALSYM(IOFSY+60)*XJJP1
      DO 120 JL=1,NDIMST
      DO 120 IR=1,NDIMST
      HMAT(ICOLOF+IR,IROWOF+JL)=HMAT(ICOLOF+IR,IROWOF+JL)
     1                       +(GCOR1+GCOR3)
     1                       *SMAT(ISTOFF+IR,ISTOFF+JL,KNDEX)
120   HMAT(IROWOF+IR,ICOLOF+JL)=HMAT(IROWOF+IR,ICOLOF+JL)
     1                 +(GGMAT+G+GCOR1+GCOR3)
     1                 *SMAT(ISTOFF+IR,ISTOFF+JL,KNDEX)
C
C 3. TERMS CONTAINING Y3
C
      KNDEX=KNDEX+1
      IF (.NOT. SYMM) THEN
                      G = CONRR*VALASY(IOFAS+1)
     1                  + VALASY(IOFAS+7)
     2                  + CONXX*(VALASY(IOFAS+24)+VALASY(IOFAS+30))*XJK
     3                  + CONZZ*VALASY(IOFAS+36)
                      GCOR1= CONCOR*AA1*VALSYM(IOFSY+27)
                      GCOR3=-CONCOR*AA3*VALASY(IOFAS+16)
     1                     +2.0E+00*CONCOR*AA3*VALASY(IOFAS+18)
      ENDIF
      IF (KP1 .EQ. 2 .AND. .NOT. SYMM)
     1      G = G + 0.5E+00*CONXX*VALASY(IOFAS+42)*XJJP1
      DO 140 JL=1,NDIMST
      DO 140 IR=1,NDIMST
      HMAT(ICOLOF+IR,IROWOF+JL)=HMAT(ICOLOF+IR,IROWOF+JL)
     1                       +(GCOR1+GCOR3)
     1                       *SMAT(ISTOFF+IR,ISTOFF+JL,KNDEX)
140   HMAT(IROWOF+IR,ICOLOF+JL)=HMAT(IROWOF+IR,ICOLOF+JL)
     1                 +(GGMAT+G+GCOR1+GCOR3)
     1                 *SMAT(ISTOFF+IR,ISTOFF+JL,KNDEX)
C
C  4. TERMS CONTAINING Y1*Y1
C     CONTRIBUTIONS FROM:   A. MU(RHO,RHO)0
C                           B. THE POTENTIAL ENERGY FUNCTION
C                           C. MUXX0+MUYY0
C                           D. MUZZ0
C                           E. CORIOLIS COMMUTATOR TERMS
C
      KNDEX=KNDEX+1
      G = CONRR*VALSYM(IOFSY+3)
     1  + VALSYM(IOFSY+12)
     1  + CONXX*(VALSYM(IOFSY+34)+VALSYM(IOFSY+43))*XJK
     2  + CONZZ*VALSYM(IOFSY+52)
      GCOR1=-2.0E+00*CONCOR*AA1*VALSYM(IOFSY+25)
     1     +3.0E+00*CONCOR*AA1*VALSYM(IOFSY+28)
      IF (SYMM) THEN
            GCOR3= CONCOR*AA1*VALSYM(IOFSY+31)
      ELSE
            GCOR3= CONCOR*AA3*VALASY(IOFAS+22)
      ENDIF
      IF (KP1 .EQ. 2)
     1      G = G + 0.5E+00*CONXX*VALSYM(IOFSY+61)*XJJP1
      DO 160 JL=1,NDIMST
      DO 160 IR=1,NDIMST
      HMAT(ICOLOF+IR,IROWOF+JL)=HMAT(ICOLOF+IR,IROWOF+JL)
     1                       +(GCOR1+GCOR3)
     1                       *SMAT(ISTOFF+IR,ISTOFF+JL,KNDEX)
160   HMAT(IROWOF+IR,ICOLOF+JL)=HMAT(IROWOF+IR,ICOLOF+JL)
     1                       +(G+GCOR1+GCOR3)
     1                       *SMAT(ISTOFF+IR,ISTOFF+JL,KNDEX)
C
C 5. TERMS CONTAINING Y3*Y3
C
      KNDEX=KNDEX+1
      IF (.NOT. SYMM) THEN
                      G = CONRR*VALASY(IOFAS+2)
     1                  + VALASY(IOFAS+8)
     2                  + CONXX*(VALASY(IOFAS+25)+VALASY(IOFAS+31))*XJK
     3                  + CONZZ*VALASY(IOFAS+37)
                    GCOR1= CONCOR*AA1*VALSYM(IOFSY+31)
                    GCOR3=-2.0E+00*CONCOR*AA3*VALASY(IOFAS+18)
     1                   +3.0E+00*CONCOR*AA3*VALASY(IOFAS+21)
      ENDIF
      IF (KP1 .EQ. 2 .AND. .NOT. SYMM)
     1      G = G + 0.5E+00*CONXX*VALASY(IOFAS+43)*XJJP1
      DO 180 JL=1,NDIMST
      DO 180 IR=1,NDIMST
      HMAT(ICOLOF+IR,IROWOF+JL)=HMAT(ICOLOF+IR,IROWOF+JL)
     1                       +(GCOR1+GCOR3)
     1                       *SMAT(ISTOFF+IR,ISTOFF+JL,KNDEX)
180   HMAT(IROWOF+IR,ICOLOF+JL)=HMAT(IROWOF+IR,ICOLOF+JL)
     1                       +(G+GCOR1+GCOR3)
     1                       *SMAT(ISTOFF+IR,ISTOFF+JL,KNDEX)
C
C 6. TERMS CONTAINING Y1*Y3
C
      KNDEX=KNDEX+1
      G = CONRR*VALSYM(IOFSY+4)
     1  + VALSYM(IOFSY+13)
     2  + CONXX*(VALSYM(IOFSY+35)+VALSYM(IOFSY+44))*XJK
     3  + CONZZ*VALSYM(IOFSY+53)
      GCOR1=-CONCOR*AA1*VALSYM(IOFSY+27)
     1     +2.0E+00*CONCOR*AA1*VALSYM(IOFSY+30)
      IF (SYMM) THEN
            GCOR3=GCOR1
      ELSE
            GCOR3=-CONCOR*AA3*VALASY(IOFAS+19)
     1           +2.0E+00*CONCOR*AA3*VALASY(IOFAS+23)
      ENDIF
      GGMAT=-CONG13*AA1*AA3*VALSYM(IOFSY+22)/2.0E+00
      IF (KP1 .EQ. 2)
     1      G = G + 0.5E+00*CONXX*VALSYM(IOFSY+62)*XJJP1
      DO 200 JL=1,NDIMST
      DO 200 IR=1,NDIMST
      HMAT(ICOLOF+IR,IROWOF+JL)=HMAT(ICOLOF+IR,IROWOF+JL)
     1                       +(GCOR1+GCOR3)
     1                       *SMAT(ISTOFF+IR,ISTOFF+JL,KNDEX)
200   HMAT(IROWOF+IR,ICOLOF+JL)=HMAT(IROWOF+IR,ICOLOF+JL)
     1                 +(GGMAT+G+GCOR1+GCOR3)
     1                 *SMAT(ISTOFF+IR,ISTOFF+JL,KNDEX)
C
C  7. TERMS CONTAINING Y1*Y1*Y1
C     CONTRIBUTIONS FROM:   A. MU(RHO,RHO)0
C                           B. THE POTENTIAL ENERGY FUNCTION
C                           C. MUXX0+MUYY0
C                           D. MUZZ0
C                           E. CORIOLIS COMMUTATOR TERMS
C
      KNDEX=KNDEX+1
      G = CONRR*VALSYM(IOFSY+5)
     1  + VALSYM(IOFSY+14)
     1  + CONXX*(VALSYM(IOFSY+36)+VALSYM(IOFSY+45))*XJK
     2  + CONZZ*VALSYM(IOFSY+54)
      GCOR1=-3.0E+00*CONCOR*AA1*VALSYM(IOFSY+28)
      IF (KP1 .EQ. 2)
     1      G = G + 0.5E+00*CONXX*VALSYM(IOFSY+63)*XJJP1
      DO 220 JL=1,NDIMST
      DO 220 IR=1,NDIMST
      HMAT(ICOLOF+IR,IROWOF+JL)=HMAT(ICOLOF+IR,IROWOF+JL)
     1                 +GCOR1*SMAT(ISTOFF+IR,ISTOFF+JL,KNDEX)
220   HMAT(IROWOF+IR,ICOLOF+JL)=HMAT(IROWOF+IR,ICOLOF+JL)
     1                 +(G+GCOR1)*SMAT(ISTOFF+IR,ISTOFF+JL,KNDEX)
C
C 8. TERMS CONTAINING Y3*Y3*Y3
C
      KNDEX=KNDEX+1
      IF (.NOT. SYMM) THEN
                      G = CONRR*VALASY(IOFAS+3)
     1                  + VALASY(IOFAS+9)
     2                  + CONXX*(VALASY(IOFAS+26)+VALASY(IOFAS+32))*XJK
     3                  + CONZZ*VALASY(IOFAS+38)
                    GCOR1=-3.0E+00*CONCOR*AA3*VALASY(IOFAS+21)
      ENDIF
      IF (KP1 .EQ. 2 .AND. .NOT. SYMM)
     1      G = G + 0.5E+00*CONXX*VALASY(IOFAS+44)*XJJP1
      DO 240 JL=1,NDIMST
      DO 240 IR=1,NDIMST
      HMAT(ICOLOF+IR,IROWOF+JL)=HMAT(ICOLOF+IR,IROWOF+JL)
     1                 +GCOR1*SMAT(ISTOFF+IR,ISTOFF+JL,KNDEX)
240   HMAT(IROWOF+IR,ICOLOF+JL)=HMAT(IROWOF+IR,ICOLOF+JL)
     1               +(G+GCOR1)*SMAT(ISTOFF+IR,ISTOFF+JL,KNDEX)
C
C  9. TERMS CONTAINING Y1*Y1*Y3
C     CONTRIBUTIONS FROM:   A. MU(RHO,RHO)0
C                           B. THE POTENTIAL ENERGY FUNCTION
C                           C. MUXX0+MUYY0
C                           D. MUZZ0
C                            E. CORIOLIS COMMUTATOR TERMS
C
      KNDEX=KNDEX+1
      G = CONRR*VALSYM(IOFSY+6)
     1  + VALSYM(IOFSY+15)
     1  + CONXX*(VALSYM(IOFSY+37)+VALSYM(IOFSY+46))*XJK
     2  + CONZZ*VALSYM(IOFSY+55)
      GCOR1=-2.0E+00*CONCOR*AA1*VALSYM(IOFSY+30)
      IF (SYMM) THEN
            GCOR3=-CONCOR*AA1*VALSYM(IOFSY+31)
      ELSE
            GCOR3=-CONCOR*AA3*VALASY(IOFAS+22)
      ENDIF
      IF (KP1 .EQ. 2)
     1      G = G + 0.5E+00*CONXX*VALSYM(IOFSY+64)*XJJP1
      DO 260 JL=1,NDIMST
      DO 260 IR=1,NDIMST
      HMAT(ICOLOF+IR,IROWOF+JL)=HMAT(ICOLOF+IR,IROWOF+JL)
     1           +(GCOR1+GCOR3)*SMAT(ISTOFF+IR,ISTOFF+JL,KNDEX)
260   HMAT(IROWOF+IR,ICOLOF+JL)=HMAT(IROWOF+IR,ICOLOF+JL)
     1           +(G+GCOR1+GCOR3)*SMAT(ISTOFF+IR,ISTOFF+JL,KNDEX)
C
C 10. TERMS CONTAINING Y1*Y3*Y3
C
      KNDEX=KNDEX+1
      IF (.NOT. SYMM) THEN
                      G = CONRR*VALASY(IOFAS+4)
     1                  + VALASY(IOFAS+10)
     2                  + CONXX*(VALASY(IOFAS+27)+VALASY(IOFAS+33))*XJK
     3                  + CONZZ*VALASY(IOFAS+39)
                    GCOR3=-2.0E+00*CONCOR*AA3*VALASY(IOFAS+23)
                    GCOR1=-CONCOR*AA1*VALSYM(IOFSY+30)
      ENDIF
      IF (KP1 .EQ. 2 .AND. .NOT. SYMM)
     1      G = G + 0.5E+00*CONXX*VALASY(IOFAS+45)*XJJP1
      DO 280 JL=1,NDIMST
      DO 280 IR=1,NDIMST
      HMAT(ICOLOF+IR,IROWOF+JL)=HMAT(ICOLOF+IR,IROWOF+JL)
     1                  +(GCOR1+GCOR3)*SMAT(ISTOFF+IR,ISTOFF+JL,KNDEX)
280   HMAT(IROWOF+IR,ICOLOF+JL)=HMAT(IROWOF+IR,ICOLOF+JL)
     1              +(G+GCOR1+GCOR3)*SMAT(ISTOFF+IR,ISTOFF+JL,KNDEX)
C
C  11. TERMS CONTAINING Y1*Y1*Y1*Y1
C     CONTRIBUTIONS FROM:   A. MU(RHO,RHO)0
C                           B. THE POTENTIAL ENERGY FUNCTION
C                           C. MUXX0+MUYY0
